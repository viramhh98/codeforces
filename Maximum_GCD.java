import java.util.Scanner;

public class Maximum_GCD {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int test_case=sc.nextInt();
        for (int i = 0; i < test_case; i++) {
            long a=sc.nextInt();
            System.out.println((int)a/2);
        }
    }
}
